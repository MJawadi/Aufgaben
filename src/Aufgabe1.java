public class Aufgabe1 {

	public static void main(String[] args) {

		System.out.println("\nAufgabe 1: Erstelle ein Java-Programm, in dem zwei Variablen für Gleitpunktzahlen");
		System.out.println(" definiert und mit den Werten 123.456 und 76.543 initialisiert \n werden. Anschließend sind die Summe und die Differenz auszugeben.");
		double num1, num2;
		double sum, difference;
		num1 = 123.456;
		num2 = 76.543;
		sum = num1 + num2;
		difference = num1 - num2;
		System.out.println("\nSumme: " + sum);
		System.out.println("Differenz: " + difference);
	}

}
